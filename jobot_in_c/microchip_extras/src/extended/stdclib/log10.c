//----------------------------------------------------------------------------------------------
// float log10(float x)
//
// Description:
//
//  Base 10 logarithm
//
// Input:
//  float x
//
// Output:
//  log10(x)
//
//----------------------------------------------------------------------------------------------
#include <errno.h>
#include <math.h>

#define C3          0.43429448190325182765f             /* log10(e) */

float log10(float x)
{
    return(log(x) * C3);
}

